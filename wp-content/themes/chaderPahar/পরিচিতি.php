    <script>
        document.addEventListener('DOMContentLoaded', function () {
            const tabItems = document.querySelectorAll('.pahar-tab-menu ul li');
            const tabContents = document.querySelectorAll('.pahar-tab-content');

            tabItems.forEach(function (item) {
                item.addEventListener('click', function (e) {
                    e.preventDefault();

                    // Remove active from all tabs
                    tabItems.forEach(function (t) { t.classList.remove('active'); });

                    // Hide all contents
                    tabContents.forEach(function (c) { c.style.display = 'none'; });

                    // Set clicked tab active
                    item.classList.add('active');

                    // Show matching content
                    const targetId = item.getAttribute('data-tab');
                    const targetContent = document.getElementById(targetId);
                    if (targetContent) {
                        targetContent.style.display = 'block';
                    }
                });
            });
            // ── Chobi gallery JS pagination ──
            (function() {
                var gallery = document.getElementById('chobi-gallery');
                var paginationEl = document.getElementById('chobi-pagination');
                if (!gallery || !paginationEl) return;

                var items = Array.prototype.slice.call(gallery.querySelectorAll('.chobi-item'));
                var perPage = <?php echo intval( $chobi_per_page ); ?>;
                var totalPages = Math.ceil(items.length / perPage);
                var currentPage = 1;
                var bnDigits = ['০','১','২','৩','৪','৫','৬','৭','৮','৯'];

                function toBanglaNum(n) {
                    return String(n).split('').map(function(d){ return bnDigits[parseInt(d)]; }).join('');
                }

                function showPage(page) {
                    currentPage = page;
                    var start = (page - 1) * perPage;
                    var end = start + perPage;
                    items.forEach(function(item, i) {
                        item.style.display = (i >= start && i < end) ? '' : 'none';
                    });
                    renderPagination();
                }

                function renderPagination() {
                    if (totalPages <= 1) { paginationEl.innerHTML = ''; return; }
                    var html = '';
                    var windowSize = 3;

                    // Prev button
                    html += '<a href="#" class="chobi-nav-btn' + (currentPage === 1 ? ' disabled' : '') + '" data-page="' + (currentPage - 1) + '">পূর্ববর্তী</a>';

                    // Sliding window of page numbers
                    var startPage = currentPage - Math.floor(windowSize / 2);
                    if (startPage < 1) startPage = 1;
                    var endPage = startPage + windowSize - 1;
                    if (endPage > totalPages) {
                        endPage = totalPages;
                        startPage = endPage - windowSize + 1;
                        if (startPage < 1) startPage = 1;
                    }

                    for (var i = startPage; i <= endPage; i++) {
                        html += '<a href="#" class="chobi-page-link' + (i === currentPage ? ' active' : '') + '" data-page="' + i + '">' + toBanglaNum(i) + '</a>';
                    }

                    // Next button
                    html += '<a href="#" class="chobi-nav-btn' + (currentPage === totalPages ? ' disabled' : '') + '" data-page="' + (currentPage + 1) + '">পরবর্তী</a>';

                    paginationEl.innerHTML = html;
                    paginationEl.querySelectorAll('.chobi-page-link, .chobi-nav-btn').forEach(function(link) {
                        link.addEventListener('click', function(e) {
                            e.preventDefault();
                            var page = parseInt(this.getAttribute('data-page'));
                            if (page >= 1 && page <= totalPages) {
                                showPage(page);
                                gallery.scrollIntoView({ behavior: 'smooth', block: 'start' });
                            }
                        });
                    });
                }

                showPage(1);
            })();

        });
    </script>
